//Funcionalidad para la sección de lugares

//Esto es una mejora, con la intención de que cuando se haga click sobre algún lugar se muestre un detalle del mismo


function showDetalleLugar(id) {
    document.getElementById(id).style.display = "block";
}

function hideDetalleLugar(id) {
    document.getElementById(id).style.display='none';
}